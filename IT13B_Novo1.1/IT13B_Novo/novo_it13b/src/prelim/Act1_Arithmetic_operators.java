public class Act1_Arithmetic_operators {
    public static void main(String[] args){
        
        int X = 10;
        int Y  = 4;
        int Z = 6;
        
      System.out.println(X + "*" + Y + "+" + Z + "=" + (X * Y + Z));
      System.out.println(X + "-" + Y + "%" + Z + "=" + ((X - Y) / Z));
      System.out.println(X + "+" + Y + "+" + Z + "=" + (X + Y + Z));
      System.out.println(X + "*" + Y + "-" + Y + "*" + Y + "=" + (X * Z - Y * Y));
      
               
    }

}
